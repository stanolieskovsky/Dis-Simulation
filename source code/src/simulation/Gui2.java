    public static String dateZapis(Date date) {
        return (date.getYear() - 70) + "-" + (date.getMonth()) + "-" + (date.getDate() - 1) + "-" + (date.getHours() - 1) + "-" + date.getMinutes() + "-" + date.getSeconds();
    }

