    private void jBStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBStartActionPerformed
         ms.stopSimulation();
        ms = new MySimulation();
        ms.setListVozidiel(listVGui);
        ms.nulujAuta();
        jTable1.setModel(new TabModel(ms.getListVozidiel()));
        jStatHlavna.setText("  ");
        // ms.setSimSpeed(0.8, 0.01d);
        if (ms.getListVozidiel().size() == 0) {
            jTextArea4.setText("nie je vybrane ziadne vozidlo");
        } else {
            setsimspeed(jSlider1, jSlider2);
            if (jComboBox1.getSelectedIndex() == 0) {
                ms.setIsOdstaveny(false);
            } else {
                ms.setIsOdstaveny(true);
            }

            ms.registerDelegate(this);
            if (jCheckBox2.isSelected()) {
                ms.setIsGrafN(true);
                jScrollPane1.getViewport().add(ms.vratGraphN().getChartPanel());
            } else {
                ms.setIsGrafN(false);
            }
            if (jCheckBox3.isSelected()) {
                ms.setIsGrafV(true);
                jScrollPane16.getViewport().add(ms.vratGraphV().getChartPanel());
            } else {
                ms.setIsGrafV(false);
            }
            if (jCheckBox1.isSelected()) {
                ms.setIsDruhy(true);
                jTextArea4.setText(ms.vypisFront(ms.getListVozidiel()));
            } else {
                ms.setIsDruhy(false);
                jTextArea4.setText(ms.vypisFront(ms.getListVozidiel()));
            }
            ms.setFinishArea(jStatHlavna);
            ms.setOdsadenie(Integer.parseInt(jSpinner1.getValue().toString()));
            jBStart.setFocusable(false);

            ms.simulate(Integer.parseInt(jTextFPocetReplikaci.getText().trim()));

        }
    }//GEN-LAST:event_jBStartActionPerformed

